# Lambda Functions: They are anonumous and inline functions.

square = lambda x: x*x
# The above is read as: This function takes x and returns x*x

print(square(3))

sum = lambda x,y: x+y
# The sum function takes 2 variables x and y and returns x+y
print(sum(3,6))

