# List Methods...

marks = [5, 2, 21, 5, 7]
extra_marks = [53, 23, 32]

print(marks)

marks.append(63) # This will add 63 at the end of the list, here our list is marks

print(marks)

marks.pop() # This will remove the last element from the list

print(marks)

marks.extend(extra_marks) # This will append another list (here extra_marks) to the list being extended (here - marks)

print(marks)

# Similarly, you can use different methods. The IDE will prompt the attached list method when you will do "list_name." here it will suggest the methods and you can explore.